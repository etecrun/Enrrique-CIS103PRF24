import math
Width = float( input('What is the width of the rectangle? '))
Recheight = float( input('What is the height of the rectangle? '))
Recarea = Width* Recheight
print('\n')
print('The area of the rectangle is :' , Recarea)
print('\n')
Tribase = float(input('What is the base of the triangle? '))
Triheight = float(input('What is the height of the triangle? '))
Triarea = 1/2 * Tribase * Triheight
print('\n')
print('The area of the triangle is :', Triarea)
print('\n')
Radius = float(input('What is the radius of the circle? '))
Circarea = math.pi * Radius**2
print('\n')
print('The area of the circle is: ', Circarea)
